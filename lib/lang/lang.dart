class LanguageText {
  final String message;
  final String? action;
  const LanguageText(this.message, {this.action});

  @override
  String toString() {
    return message;
  }
}

final languages = <String, String>{
  'zh_Hans_': 'zh-CHS',
  'en': 'en',
};

String resolveSystemLanguage(String deviceLocale) {
  for (var key in languages.keys) {
    if (deviceLocale.startsWith(key)) {
      return languages[key]!;
    }
  }

  return 'en';
}
